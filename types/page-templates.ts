/**
 * ServicePageData — the complete data contract for service pages.
 *
 * Usage:
 * const pageData: ServicePageData = { ... }
 * export default function Page() {
 *   return <ServicePage data={pageData} />
 * }
 *
 * Image mapping:
 * The CRM Page Manager generates the `images` block.
 * All other fields are content — managed in the page file.
 */
export interface ServicePageData {
  // SEO layer
  seo:            SEOMeta

  // Hero section
  h1:             string    // Focus keyword in first 4 words
  subheadline:    string    // USP reinforcement — 1 sentence
  quickAnswer:    string    // AEO block — 2-3 sentences. Direct answer

  // Intro section
  intro:          string    // 2-3 paragraphs. B2B positioning. HTML ok

  // Services grid
  services:       ServiceCard[]   // Exactly 6 cards

  // Process section
  processSteps:   ProcessStep[]   // Exactly 3 steps

  // EEAT section
  eeatPoints:     TrustPoint[]    // Exactly 4 points
  eeatHeadline?:  string          // "Why London businesses choose Vigil"

  // Compliance
  complianceTags: ComplianceTag[] // 4-6 tags

  // Case study (optional)
  caseStudy?:     CaseStudy

  // Borough coverage
  boroughLinks:   BoroughLink[]   // All live borough pages for this division

  // FAQ
  faqs:           FAQ[]           // Minimum 5 questions

  // CTA
  cta:            CTAConfig

  // Images — generated by CRM Page Manager
  // All images defined here. Template reads from this block only.
  images:         ImageSlots

  // Schema — passed to JSON-LD script in metadata
  schema?: {
    serviceType: string
    areaServed:  string
  }
}

/**
 * BoroughPageData — data contract for borough/location pages.
 *
 * Simpler than ServicePage — fewer sections, local focus.
 * Borough agent generates these autonomously.
 */
export interface BoroughPageData {
  // SEO
  seo:            SEOMeta

  // Location context
  borough:        string    // "Westminster"
  region:         string    // "Central London"
  postcodes:      string[]  // ["SW1", "WC1", "W1"]

  // Content
  h1:             string    // "[Service] [Borough]" exact match
  quickAnswer:    string    // AEO block — borough-specific
  intro:          string    // 2 paragraphs. Borough-specific content
  localContext:   string    // Local landmarks, sectors, business types

  // Services (subset relevant to borough)
  services:       ServiceCard[]   // 4-6 cards

  // Local trust
  eeatPoints?:    TrustPoint[]    // Optional — 3 points

  // Nearby boroughs (internal linking)
  nearbyBoroughs: BoroughLink[]   // 3-5 adjacent boroughs

  // FAQ
  faqs:           FAQ[]           // Minimum 5 — borough-specific

  // CTA
  cta:            CTAConfig

  // Images — generated by CRM Page Manager + borough agent
  images:         BoroughImageSlots

  // Schema
  schema?: {
    borough:     string
    areaServed:  string[]
    serviceType: string
  }
}

/**
 * BlogPostData — data contract for blog posts.
 *
 * Content agent generates new posts conforming to this interface.
 */
export interface BlogPostData {
  // SEO
  seo:          SEOMeta

  // Post metadata
  h1:           string    // Question-format headline preferred
  published:    string    // ISO date string
  updated?:     string    // ISO date string — for freshness
  author:       string    // "Vigil Cleaning Services team"
  readTime?:    number    // Minutes

  // Content
  intro:        string    // 1 paragraph. Hook + quick answer
  body:         string    // Main content. HTML. Min 800 words
  conclusion:   string    // 1 paragraph. Soft CTA

  // Optional FAQ
  faqs?:        FAQ[]     // If present: FAQPage schema added

  // Related content
  relatedPosts: RelatedPost[]   // 2-3 related articles

  // CTA
  cta:          CTAConfig

  // Images
  images:       BlogImageSlots

  // Tags for content matching
  tags?:        string[]   // service types, sectors
}

/**
 * LocationHubData — hub page aggregating all borough pages.
 * /commercial-cleaning-london/ and /commercial-security-london/
 * One per site. Links to all borough pages.
 */
export interface LocationHubData {
  // SEO
  seo:          SEOMeta

  // Content
  h1:           string
  quickAnswer:  string
  intro:        string

  // All boroughs for this division — live and coming soon
  allBoroughs:  BoroughLink[]

  // Featured boroughs (high commercial value)
  featuredBoroughs: BoroughLink[]

  // Services overview
  services:     ServiceCard[]

  // FAQ
  faqs:         FAQ[]

  // CTA
  cta:          CTAConfig

  // Images
  images: {
    hero:   ImageSlot   // 1200×630
    og:     ImageSlot   // Inherits hero
  }
}

// ─────────────────────────────────────────────────────────────────────────────
// CRM INTEGRATION TYPES
// These types are used by the CRM Page Manager
// to generate the images block for each page.
// ─────────────────────────────────────────────────────────────────────────────

/**
 * Page slot definition — stored in page_slot_templates table in CRM.
 * Defines what image slots a page type has.
 */
export interface PageSlotDefinition {
  slotKey:      keyof ImageSlots | keyof BoroughImageSlots | keyof BlogImageSlots
  label:        string
  purpose:      string
  width:        number
  height:       number
  aspectRatio:  string
  required:     boolean
  isPriority:   boolean  // Next.js priority prop
  isOg:         boolean  // Used in OG metadata
  inheritFrom?: string   // Slot key to inherit if empty
  imageType:    string   // Matches images.image_type in CRM
}

/**
 * Page image assignment — stored in page_image_assignments table.
 * Links a CRM image to a slot on a specific page.
 */
export interface PageImageAssignment {
  pageUrl:   string
  slotKey:   string
  imageId:   number    // CRM images.id
  imageSrc:  string    // Cloudinary URL
  imageAlt:  string    // From CRM AI Vision enrichment
  width:     number
  height:    number
  assignedAt: string
}

/**
 * Generated images block — output from CRM Page Manager.
 * Paste this const block at the top of any page file.
 * Replace the existing images const to update all images.
 */
export interface GeneratedImagesBlock {
  pageUrl:      string
  pageType:     'service' | 'borough' | 'blog' | 'hub'
  generatedAt:  string
  slots:        Partial<ImageSlots & BoroughImageSlots & BlogImageSlots>
  // The const block to paste into the page file:
  codeBlock:    string
}
